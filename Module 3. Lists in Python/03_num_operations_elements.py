numbers = [10, 4, -1, 20, 5.5, 7, 1]

# smallest number in the list
# min()
# print(numbers.min()) # AttributeError: 'list' object has no attribute 'min'
print(min(numbers)) # -1


# Biggest number in the list
# max()
print(max(numbers)) # 20


# Total of the numbers in the list
# sum()
print(sum(numbers)) # 46.5